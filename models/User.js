const { Model, DataTypes } = require('sequelize')

const sequelize = require('../config')

class User extends Model { }

User.init(
  {
    id: {
      type: DataTypes.INTEGER,
      allowNull: false,
      primaryKey: true,
      autoIncrement: true
    },
    username: {
      type: DataTypes.STRING,
      allowNull: false
    },
    password: {
      type: DataTypes.STRING,
      allowNull: false,
      validate: {
        len: [6]
      }
    }
  },
  {
    sequelize,
    timestamps: false,
    modelName: "users"
  }
)

module.exports = User